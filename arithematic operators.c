#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a=25,b=10,c;

    c=a+b;
    printf("The addition of a and b is.: %d \n",c);

    c=a-b;
    printf("the subtraction of a and b.: %d \n",c);

    c=a*b;
    printf("the multiplication of a and b.: %d \n",c);

    c=a/b;
    printf("the division of a and b.: %d \n",c);

    c=a%b;
    printf("the remainder is.: %d \n",c);

    return 0;
}
